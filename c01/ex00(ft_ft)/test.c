/* Эта функция принимает адрес переменной(или массива) и меняет содержимое на своё */

void	ft_ft(int *nbr)		/* СЮДА(в указатель nbr(он обьявлен в скобках)) ПРИНИМАЕТСЯ И ЗАПИСЫВАЕТСЯ АДРЕСС  */
{
	*nbr = 42;				/* С помощью операции разыменовывания(*nbr) меняем данные(*nbr = 42), которые хранятся по адресу который мы получили */
}


int		main(void)
{
	int		i;				/* Обьявляем мпеременную типа инт, котрая будет хранить число для прверки*/

	i = 21;					/*Запишем туда число 21 */

	ft_ft(&i);				/* Вызываем функцию и отправляем адрес этой переменной в аргументы функции.
							** Эта функция должна принять адрес этой переменной и перейдя по нему поменять данные с 21 на 42,
							** которые хранятся в переменной */

	ft_putnbr(i);			/* А с помощью этой функции выведем содержимое переменной i. Нам нужно поверить изменилось ли число с 21 на 42 */

	return (0);
}


