int			ft_list_size(t_list *begin_list)/* Функция внутри текущей структуры считает количество экземпляров структур связанных между собой последовательно */
{
	int		count;						/* Обьявим переменную для хранения количества найденых структур */

	count = 0;							/* Инициализируем нулем переменную для хранения количества найденых структур */
	while (begin_list)					/* Запускаем цикл который пройдется вглубь цепочки связанных между собой экземпляров структуры типа 't_list' */
	{
		count++;						/* Считаем каждую структуру в которую мы можем попасть */
		begin_list = begin_list->next;	/* В указателе на экземпляр структуры типа 't_list' сохраняем адресс экземпляра структуры типа 't_list', который
										 * может храниться в одном из элементов текущего экземпляра структуры 't_list' (list->next) */
	}
	return (count);						/* Завершаем функцию и возвращаем колеичество посчитаных экземпляров структур типа 't_list' */
}



