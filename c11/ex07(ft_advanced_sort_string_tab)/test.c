#include <unistd.h>				/* Подключаем библиотеку содержащую функцию "write" */
#include <stdlib.h>				/* Подкоючаем библиотеку содержащую функцию для выделения 
								 * памяти(malloc) и функцию для подсчета размера (sizeof) 
								 * и функцию(free) для освобождения выделеной памяти */

/* ************************************************************************** */

void	ft_putchar(char c)		/* Функция печати символа */
{
	write(1, &c, 1);
}

/* ************************************************************************** */

void	ft_putstr(char *str)	/* Функция печати строки */
{
	while(*str)
		ft_putchar(*str++);
}

/* ************************************************************************** */

void	ft_putstr_tab(char **tab)/* Функция печати массива строк */
{
	while(*tab)
	{
		ft_putstr(*tab++);
		ft_putchar('\n');
	}
}

/* ************************************************************************** */

void 	ft_destroy(char **factory)/* Функция освобождения выделеной памяти */
{
	int i;

	i = 0;
	while (factory[i] != ((void *)0))
	{
		free(factory[i]);
		i++;
	}
	free(factory);
}

