int		ft_strcmp(char *s1, char *s2)/* Функция сравнивает два символа и возвращает их разницу */
{
	while (*s1 && (*s1 == *s2))		/* Если символ в первой строке не равен смиволу завершающего нуля и равен символу 
									 * во второй строке на той же позиции то мы */
	{
		s1++;						/* Сдвигаем адрес в указателе вперед для перехода к след элементу первой строки */
		s2++;						/* Сдвигаем адрес в указателе вперед для перехода к след элементу второй строки */
	}
	return (*s1 - *s2);				/* Если символ в первой строке равен смиволу завершающего нуля или не равен символу
									 * во второй строке то мы завершаем функцию и возвращаем разность этих символов */
}

void	ft_swap(char **x, char **y)	/* Функция меняет местами адреса в указателях хранящих адреса указателей */
{
	char *temp;

	temp = *x;
	*x = *y;
	*y = temp;
}

int		getlength(char **tab)		/* Функция проходится по элементам массива указателей и считает их количество  */
{
	int i;							/* Обьявляем счетчик для перемеещения по массиву указателей */

	i = 0;							/* Инициализируем нулем счетчик для перемеещения по массиву указателей */
	while (*tab++)					/* Запускаем цикл, который пройдется по каждому элементу массива указателей, до тех пор пока они не закончатся */
		i++;						/* Увеличиваем счетчик для перехода к след элементу */
	return (i);						/* Завершаем функцию и возвращаем значение переменной 'i', которая хранит количество пройденых элементов */
}

void	ft_advanced_sort_string_tab(char **tab, int(*cmp)(char *, char *))	/* Функция сортирует массив со строками по возростанию.
									 * Она принимает в аргументах - адрес массива строк и адрес функции(в текущем случае мы
									 * будем принимать адрес функции 'ft_strcmp' для сравнения двух строк) */
{
	int i;							/* Обьявляем счетчик для перемеещения по массиву со строками */
	int j;							/* Обьявляем счетчик для многократного перемеещения по массиву со строками */
	int size;						/* Обьявляем переменную для хранению количества строк */

	i = 0;							/* Инициализируем нулем счетчик для перемеещения по массиву указателей */
	size = getlength(tab);			/* вызываем функцию подсчетам количества строк и сохраняем результат в переменной */
	while (i < size - 1)			/* До тех пор пока мы не дойдем до конца массива строк запускаем этот цикл.
									 * В этом цикле мы будем запускать другой цикл, который за каждую итерацию первого
									 * цикла будет перемещать один самый больший элемент массива строк в конец */
	{
		j = 0;						/* Обнуляем счетчик для многократного перемеещения по массиву со строками */
		while (j < size - i - 1)	/* До тех пор пока мы не дойдем до конца массива строк запускаем этот цикл.
									 * Этот цикл будет искать самый большой элемент массива строк и перемещать его в конец массива */
		{
			if (cmp(tab[j], tab[j + 1]) > 0)	/* Сравниваем строки. Если символы текущей строки больше чем символы следующей */ 
			{
				ft_swap(&tab[j], &tab[j + 1]);	/* То меняем строки местами */
			}
			j++;					/* Увеличиваем счетчик для перехода к сравнению следующих по порядку строк */
		}
		i++;						/* Увеличиваем счетчик для ограничения предела движения сортировки строк */
	}
}

int		main(void)
{
	char	*tab = "3hello 1my 2frend";	/* Обьявим указатель исохраним туда адрес тестовой строки */
	char	**point;					/* Обьявим указатель для хранения адреса массива строк */

	point = ft_split_whitespaces(tab);	/* Отправляем адрес строки в функцию ккоторая разделит строку
										 * на слова и разложит эти слова по разным строкам массива строк*/
	ft_advanced_sort_string_tab(point, &ft_strcmp);/* Отправим добытый из прошлой функции адресс массива строк и адресс фунции 'ft_strcmp'
										 * в функцию, которая отсортирует нам эти строки в порядке возрастания */
	ft_putstr_tab(point);				/* Печатаем результат */
	ft_destroy(point);					/* Освобождаем память массива указателей */
	return (0);							/* Завершаем функцию и возвращаем 0 */
}
