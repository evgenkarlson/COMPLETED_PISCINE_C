#include <unistd.h>

void	ft_generic(void)
{
	int			i;
	char		c;
	const char	*s = "Tut tut ; Tut tut";

	c = '\n';
	i = 0;
	while (s[i] != '\0')
	{
		write(1, &s[i], 1);
		i++;
	}
	write(1, &c, 1);
}





