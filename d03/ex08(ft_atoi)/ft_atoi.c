/* **************************************************************************
**  
**  
**  - Воспроизвести поведение функции atoi (man atoi).
**  
**  
** ************************************************************************** **
**  
**  
**  Аргументы:
**  
**  str – указатель на строку, которую необходимо преобразовать в число.
**  
**  Возвращаемое значение:
**  
**  Функция возвращает число - результат преобразование строки, 
**  если в начале строки содержится число. Если в начале строки 
**  стоит символ не являющейся цифрой, то функци возвращает ноль.
**  
**  
**  Описание:
**  
**  Функция atoi() преобразует строку, на которую указывает 
**  аргумент str, в число типа int. Преобразование заканчивается, 
**  когда встречается символ не являющейся цифрой или, когда будет 
**  преобразована вся строка. Если первый символ строки не цифра, 
**  то функция вернет 0 и завершит работу.
**  
**  Если число в строке превышает максимальный или минимальный размер
**  числа типа int, то функция возвращает соответственно максимально 
**  или минимально допустимое число для типа int. Возвращаемая величина
**  зависит от аппаратной платформы на которой запускается программа.
**
**  От функции atoi() отличается типом возвращаемого числа и как следствие 
**  макимальной длинной преобразуемой строки. Для 'atoi' возвращаемое число имеет
**  тип 'int', а соответственно разрядность 16 или 32 бита в зависимости от 
**  аппаратной платформы, на которой запускается программа. Для atol возвращаемое
**  число имеет тип 'long int', который всегда 32 разрядный, не зависимо от 
**  архитектуры процессора.
**
**  Так, на пример, в результате преобразования строки "217bg12" будет получено 
**  число 217, а при преобразовании строки "b237пр" будет получено число 0.
**
**
** ************************************************************************** **
**
**  Скомпилируй файл тест. В нем можно увидеть как работает эта функция вживую
**
** ************************************************************************** */
/* ************************************************************************** */


int	ft_atoi(char *str)
{
	int	i;
	int	negativ;
	int	number;

	i = 0;
	negativ = 0;
	number = 0;
	while ((str[i] == ' ') || (str[i] == '\t') || (str[i] == '\n')
		|| (str[i] == '\v') || (str[i] == '\f') || (str[i] == '\r'))
		i++;
	if (str[i] == '-')
		negativ = 1;
	if ((str[i] == '-') || (str[i] == '+'))
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		number = (number * 10) + ((int)str[i] - '0');
		i++;
	}
	if (negativ == 1)
		return (-number);
	else
		return (number);
}





